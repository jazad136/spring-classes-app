package com.jsaddlercs.classapp.model;

public class StatisticsType {
	private Integer rosterSize;
	private Integer dropped;
	private Integer semesterHours;
	
	public StatisticsType() { }
	
	
	public Integer getRosterSize() { return rosterSize; }
	public void setRosterSize(Integer rosterSize) { this.rosterSize = rosterSize; }

	public Integer getDropped() { return dropped; }
	public void setDropped(Integer dropped) { this.dropped = dropped; }

	public Integer getSemesterHours() { return semesterHours; }
	public void setSemesterHours(Integer semesterHours) { this.semesterHours = semesterHours; }
	
	
}
